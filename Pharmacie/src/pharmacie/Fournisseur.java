    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String nom = jTextField1.getText();
        String prenom = jTextField2.getText();
        String email = jTextField3.getText();
        String adresse = jTextField4.getText();
        String telephone = jTextField5.getText();
        
         try (Connection conn = DatabaseConnection.connect()) {
        String sql = "INSERT INTO fournisseurs (nom, prenom, email, adresse, telephone) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement pstmt = conn.prepareStatement(sql);
        pstmt.setString(1, nom);
        pstmt.setString(2, prenom);
        pstmt.setString(3, email);
        pstmt.setString(4, adresse);
        pstmt.setString(5, telephone);

        int rowsInserted = pstmt.executeUpdate();
        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(this, "Fournisseur enregistré avec succès !");
            loadFournisseurs();
        }
    }catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erreur lors de l'enregistrement : " + e.getMessage());
        }
         
    }//GEN-LAST:event_jButton2ActionPerformed

