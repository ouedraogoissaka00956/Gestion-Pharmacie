    private void loadFournisseurs() {
    // Effacer le JPanel avant de le remplir
    jPanel3.removeAll();
    
    // Connexion à la base de données
    try (Connection conn = DatabaseConnection.connect()) {
        String sql = "SELECT * FROM fournisseurs";
        PreparedStatement pstmt = conn.prepareStatement(sql);
        ResultSet rs = pstmt.executeQuery();

        while (rs.next()) {
            String nom = rs.getString("nom");
            String prenom = rs.getString("prenom");
            String email = rs.getString("email");
            String adresse = rs.getString("adresse");
            String telephone = rs.getString("telephone");

            // Créer une étiquette pour chaque fournisseur et l'ajouter au JPanel
            JLabel label = new JLabel(nom + " " + prenom + " - " + email + " - " + adresse + " - " + telephone);
            jPanel3.add(label);
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erreur lors du chargement des fournisseurs : " + e.getMessage());
    }

    // Actualiser le JPanel
    jPanel3.revalidate();
    jPanel3.repaint();
}
    
